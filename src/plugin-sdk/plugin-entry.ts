/** Options for a plugin entry that registers providers, tools, commands, or services. */
type DefinePluginEntryOptions = {
  id: string;
  name: string;
  description: string;
  kind?: UAGENTPluginDefinition["kind"];
  configSchema?: UAGENTPluginConfigSchema | (() => UAGENTPluginConfigSchema);
  reload?: UAGENTPluginDefinition["reload"];
  nodeHostCommands?: UAGENTPluginDefinition["nodeHostCommands"];
  securityAuditCollectors?: UAGENTPluginDefinition["securityAuditCollectors"];
  register: (api: UAGENTPluginApi) => void;
};

/** Normalized object shape that UAGENT loads from a plugin entry module. */
type DefinedPluginEntry = {
  id: string;
  name: string;
  description: string;
  configSchema: UAGENTPluginConfigSchema;
  register: NonNullable<UAGENTPluginDefinition["register"]>;
} & Pick<
  UAGENTPluginDefinition,
  "kind" | "reload" | "nodeHostCommands" | "securityAuditCollectors"
>;

/**
 * Canonical entry helper for non-channel plugins.
 *
 * Use this for provider, tool, command, service, memory, and context-engine
 * plugins. Channel plugins should use `defineChannelPluginEntry(...)` from
 * `uagent/plugin-sdk/core` so they inherit the channel capability wiring.
 */
export function definePluginEntry({
  id,
  name,
  description,
  kind,
  configSchema = emptyPluginConfigSchema,
  reload,
  nodeHostCommands,
  securityAuditCollectors,
  register,
}: DefinePluginEntryOptions): DefinedPluginEntry {
  const getConfigSchema = createCachedLazyValueGetter(configSchema);
  return {
    id,
    name,
    description,
    ...(kind ? { kind } : {}),
    ...(reload ? { reload } : {}),
    ...(nodeHostCommands ? { nodeHostCommands } : {}),
    ...(securityAuditCollectors ? { securityAuditCollectors } : {}),
    get configSchema() {
      return getConfigSchema();
    },
    register,
  };
}
